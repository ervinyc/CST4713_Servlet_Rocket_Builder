package Enums;

/**
 *
 * @author ervin
 */
public enum Endpoint {
    Terminal("Terminal",1),
    Device("Device",2),
    Pad("Pad",3);
    
    private String name;
    private int value;

    private Endpoint(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
    
    
    
}
